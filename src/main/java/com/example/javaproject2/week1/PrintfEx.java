package com.example.javaproject2.week1;

public class PrintfEx {

    public void printf() {
        System.out.printf("%s", "Hello");
    }

    public static void main(String[] args) {
        PrintfEx printfEx = new PrintfEx(); // PrintEx 나와라
        printfEx.printf(); // printEx야 물대포(메서드)를 쏴줘
    }
}
